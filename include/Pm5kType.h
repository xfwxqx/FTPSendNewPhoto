#define PM5K_TRUE				1
#define PM5K_FALSE				0
#ifndef TRUE
#define TRUE					PM5K_TRUE
#endif
#ifndef FALSE
#define FALSE					PM5K_FALSE
#endif
#define PM5K_INFINITE			(0xFFFFFFFFL)

#define PM5K_PCM_NODE_VAR_TYPE_ID_S8					0
#define PM5K_PCM_NODE_VAR_TYPE_ID_U8					1
#define PM5K_PCM_NODE_VAR_TYPE_ID_S16					2
#define PM5K_PCM_NODE_VAR_TYPE_ID_U16					3
#define PM5K_PCM_NODE_VAR_TYPE_ID_S32					4
#define PM5K_PCM_NODE_VAR_TYPE_ID_U32					5
#define PM5K_PCM_NODE_VAR_TYPE_ID_S64					6
#define PM5K_PCM_NODE_VAR_TYPE_ID_U64					7

#define PM5K_PCM_DEV_NODE_NUM_MAX						1024


/*
 ************************************************************************************************************************************************************************
 * 数据类型定义
 ************************************************************************************************************************************************************************
*/


typedef signed char				PM5K_S8;
typedef unsigned char			PM5K_U8;
typedef signed short int		PM5K_S16;
typedef unsigned short int		PM5K_U16;
typedef signed long int			PM5K_S32;
typedef unsigned long int		PM5K_U32;
typedef signed long long		PM5K_S64;
typedef unsigned long long		PM5K_U64;

typedef int						PM5K_BOOL;

typedef float					PM5K_F32;
typedef double					PM5K_F64;

typedef PM5K_U8					PM5K_BYTE;
typedef PM5K_U16				PM5K_WORD;
typedef PM5K_U32				PM5K_DWORD;

typedef union __attribute__((packed)) _PM5K_NO_ALIGN_VAR
{
	PM5K_S32		m_S32;
	PM5K_U32		m_U32;
	PM5K_F32		m_F32;
	PM5K_S64		m_S64;
	PM5K_U64		m_U64;
	PM5K_F64		m_F64;
}PM5K_NO_ALIGN_VAR,*PPM5K_NO_ALIGN_VAR;


typedef union __attribute__((packed)) _PM5K_PCM_NODE_VAR_VAL
{
	PM5K_S8			m_S8;
	PM5K_U8			m_U8;
	PM5K_S16		m_S16;
	PM5K_U16		m_U16;
	PM5K_S32		m_S32;
	PM5K_U32		m_U32;
	PM5K_S64		m_S64;
	PM5K_U64		m_U64;
}PM5K_PCM_NODE_VAR_VAL,*PPM5K_PCM_NODE_VAR_VAL;
typedef struct __attribute__((packed)) _PM5K_PCM_NODE_VAR_DESC
{
	PM5K_U8			m_Type;			/*变量类型*/
	PM5K_S8			m_Rate;			/*变量值域倍比系数，用实际倍比系数的以10为底数的对数来表示，即实际倍比系数为...0.01,0.1,1,10,100...等*/
}PM5K_PCM_NODE_VAR_DESC,*PPM5K_PCM_NODE_VAR_DESC;
typedef struct __attribute__((packed)) _PM5K_PCM_DATA_DESC
{
	PM5K_U32				m_ReadOnlyNodeNum;									/*实际只读节点总数目*/
	PM5K_U32				m_ReadWriteNodeNum;									/*实际可读可写节点总数目*/
	PM5K_PCM_NODE_VAR_DESC	m_ReadOnlyNodeDescTab[PM5K_PCM_DEV_NODE_NUM_MAX];	/*从零号节点开始依次存储各个只读节点数据描述*/
	PM5K_PCM_NODE_VAR_DESC	m_ReadWriteNodeDescTab[PM5K_PCM_DEV_NODE_NUM_MAX];	/*从零号节点开始依次存储各个读写节点数据描述*/
}PM5K_PCM_DATA_DESC,*PPM5K_PCM_DATA_DESC;




